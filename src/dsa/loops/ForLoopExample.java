package dsa.loops;

public class ForLoopExample {
    public static void main(String[] args) {



        /*
        * for loop syntax:
        *  for (initialization; condition;updation){
        *
        *       // Body of the loop
        *
        * }
        *
        *
        * */

        //Q. Print the number 1 to 10
        for (int num = 1; num <= 100; num += 1) {
            System.out.print(num+" ");

        }
    }
}
